#!/usr/bin/env python3
"""A type-annotated function to_str that takes a float n as argument and
returnsthe string representation of the float."""


def to_str(n: float) -> str:
    """
    Returns the string representation of the input float number.

    Args:
        n (float): The float number to convert to string.

    Returns:
        str: The string representation of the input number.
    """
    return str(n)
